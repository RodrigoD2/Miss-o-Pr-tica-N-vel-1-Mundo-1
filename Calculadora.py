#calculadora que deverá permitir a execução das quatro
#operações matemáticas (adição, subtração, multiplicação e
#divisão) utilizando valores digitados pelos usuários.

#1° variável inserida pelo usuário
n1 = int(input("Digite o primeiro valor: "))

#2° variável inserida pelo usuário
n2 = int(input("Digite o segundo valor: "))

#Operações matemáticas que serão executadas em nosso código
soma = n1 + n2              #Adição
subtracao = n1 - n2         #Subtração
multiplicacao = n1 * n2     #Multiplicação
divisao = n1 / n2           #Subtração

#Resulados que seram impressos na tela
print("O resultado da adição é:", soma)                    #Resultado da adição
print("O resultado da subtração é:", subtracao)            #Resultado da subtração
print("O resultado da multiplicação é:", multiplicacao)    #Resultado da multiplicação
print("O resultado da divisao é:", divisao)                #Resultado da divisão
